#!/bin/sh

awk -F'[-.]' '
/^v?[0-9]+\.[0-9]+\.[0-9]+-rc[0-9]+-[0-9]+-g[0-9a-fA-F]+(-dirty)?$/ {
    sub(/^v/, "", $1)
    print $1 "." $2 "." $3 "-0." $4 "." $5 "." $6 ($7 == "dirty" ? ".dirty" : "")
    next
}
/^v?[0-9]+\.[0-9]+\.[0-9]+-[0-9]+-g[0-9a-fA-F]+(-dirty)?$/ {
    sub(/^v/, "", $1)
    print $1 "." $2 "." ($3 + 1) "-0." $4 "." $5 ($6 == "dirty" ? ".dirty" : "")
    next
}
{
    sub(/^v/, "")
    print
}
'
