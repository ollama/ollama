// ── model metadata (hardcoded; avoids fragile gguf key probing) ─────────────

type modelMeta struct {
	name       string
	hiddenSize int
	numKVHeads int
	headDim    int
	numLayers  int
	hasBias    bool // attn_k.bias present
}

var (
	qwen25_7b  = modelMeta{name: "qwen2.5:7b", hiddenSize: 3584, numKVHeads: 4, headDim: 128, numLayers: 28, hasBias: true}
	llama32_3b = modelMeta{name: "llama3.2:3b", hiddenSize: 3072, numKVHeads: 8, headDim: 128, numLayers: 28, hasBias: false}
)

