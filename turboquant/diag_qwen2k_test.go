	fmt.Fprintf(w, "`maxSingletonEnergy` = max_i(r_i²)/sum_j(r_j²) of the rotated K. ")
	fmt.Fprintf(w, "`p99p50` = p99(|rotated|)/p50(|rotated|). ")
	fmt.Fprintf(w, "`biasCV` = stddev_across_trials(rotated) / (|mean_across_trials| + eps) averaged per dim — low = stable per-head bias.\n\n")

