//go:build tqdiag

// Package turboquant diagnostic: Phase 1 of the qwen2.5:7b tq3k fix plan.
// Runs four K-encoder variants against synthesized qwen2-like and llama-like
// K distributions, reports rel-dot-error and distribution stats, and lets
// the operator pick the right Phase 2 sub-phase from empirical data.
//
// Build-gated so normal `go test ./turboquant/...` doesn't pull it in:
//
//	go test -tags tqdiag -run TestDiagQwen2KPhase1 -v ./turboquant/...
//
// Required env vars:
//
//	TQ_MODEL_PATH_QWEN25_7B=/usr/share/ollama/.ollama/models/blobs/sha256-2bada8a7450677000f678be90653b85d364de7db25eb5ea54136ada5f3933730
//	TQ_MODEL_PATH_LLAMA32_3B=/usr/share/ollama/.ollama/models/blobs/sha256-dde5aa3fc5ffc17176b5e8bdc82f587b24b2678c6c66101bf7da77af9f7ccdff
//
// Output:
//
//	- stdout: markdown table per model: (layer, head) × variant → metrics
//	- /tmp/tq_diag_phase1.md: same data, committed as Phase 1 evidence

